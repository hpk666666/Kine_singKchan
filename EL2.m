function [P0,P1,P2,P3,P4]=EL2(u,Vt,VL,data,n)
P0=0;P1=0;P2=0;P3=0;P4=0;
for i=1:n+1
    P0=data(1,i)*1/((i+u-1)+3)*(VL-Vt).^((i+u-1)+3)...
        -data(1,i)*VL^2*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1)...
       +2*VL*(-data(1,i)*1/((i+u-1)+2)*(VL-Vt).^((i+u-1)+2)...
        +data(1,i)*VL*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1))+P0;
    P1=data(2,i)*1/((i+u-1)+3)*(VL-Vt).^((i+u-1)+3)...
        -data(2,i)*VL^2*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1)...
        +2*VL*(-data(2,i)*1/((i+u-1)+2)*(VL-Vt).^((i+u-1)+2)...
        +data(2,i)*VL*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1))+P1;
    P2=data(3,i)*1/((i+u-1)+3)*(VL-Vt).^((i+u-1)+3)...
       -data(3,i)*VL^2*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1)...
        +2*VL*(-data(3,i)*1/((i+u-1)+2)*(VL-Vt).^((i+u-1)+2)...
        +data(3,i)*VL*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1))+P2;
    P3=data(4,i)*1/((i+u-1)+3)*(VL-Vt).^((i+u-1)+3)...
        -data(4,i)*VL^2*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1)...
        +2*VL*(-data(4,i)*1/((i+u-1)+2)*(VL-Vt).^((i+u-1)+2)...
        +data(4,i)*VL*1/((i+u-1)+1)*(VL-Vt).^((i+u-1)+1))+P3;
    P4=data(5,i)*1/((i+u-1+1)+3)*(VL-Vt).^((i+u-1+1)+3)...
       -data(5,i)*VL^2*1/((i+u-1+1)+1)*(VL-Vt).^((i+u-1+1)+1)...
        +2*VL*(-data(5,i)*1/((i+u-1+1)+2)*(VL-Vt).^((i+u-1+1)+2)...
        +data(5,i)*VL*1/((i+u-1+1)+1)*(VL-Vt).^((i+u-1+1)+1))+P4;
end
end