function [P]=P_open(u,Vt,Ve,VL,data1,data2,data3,data4,data5,n,K)
P=0;
for i=1:n+1
    P=K(1)*1/(i+u(1))*data1(5,i)*(Vt-Ve)^(i+u(1))...
        +K(2)*1/(i+u(2)+1)*data2(5,i)*(VL-Vt)^(i+u(2)+1)...
        +K(3)*1/(i+u(3)+1)*data3(5,i)*(VL-Vt)^(i+u(3)+1)...
        +K(4)*1/(i+u(4)+1)*data4(5,i)*(VL-Vt)^(i+u(4)+1)...
        +K(5)*1/(i+u(5)+1)*data5(5,i)*(VL-Vt)^(i+u(5)+1)+P;
end
end